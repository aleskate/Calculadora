#include "OperacionesArimeticas.h"

OperacionesArimeticas::OperacionesArimeticas()
{
}

OperacionesArimeticas::~OperacionesArimeticas()
{
}

/*Suma dos números*/
double OperacionesArimeticas::Sumar(double x,double y){
    return x + y;
}

/*Resta dos números*/
double OperacionesArimeticas::Restar(double x,double y){
    return x - y;
}

/*Multiplica dos números*/
double OperacionesArimeticas::Multiplicar(double x,double y){
    return x * y;
}

/*Divide dos números*/
double OperacionesArimeticas::Dividir(double x,double y){
    return x / y;
}